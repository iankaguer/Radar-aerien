public class Avion {
    public String identifiant;
    Boolean auSol, enPanne;
    double abcisse, ordonne;
    int altitude;

    public String getIdentifiant() {
        return identifiant;
    }

    public void setIdentifiant(String identifiant) {
        this.identifiant = identifiant;
    }

    public Boolean getAuSol() {
        return auSol;
    }

    public void setAuSol(Boolean auSol) {
        this.auSol = auSol;
    }

    public Boolean getEnPanne() {
        return enPanne;
    }

    public void setEnPanne(Boolean enPanne) {
        this.enPanne = enPanne;
    }

    public double getAbcisse() {
        return abcisse;
    }

    public void setAbcisse(double abcisse) {
        this.abcisse = abcisse;
    }

    public double getOrdonne() {
        return ordonne;
    }

    public void setOrdonne(double ordonne) {
        this.ordonne = ordonne;
    }

    public double getAltitude() {
        return altitude;
    }

    public void setAltitude(int altitude) {
        this.altitude = altitude;
    }


}

